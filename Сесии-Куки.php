# КУКИ

Файл куки хранится в браузере клиента
В куках нельзя хранить важные данные
Кука удаляется с помощью функции установки, нужно поставить минус перед временем жизни

#Сигнатура
setcookie(
  string $name,             // Имя куки (обязательно)
  string $value = "",       // Значение куки (обязательно)
  int $expire = 0,          // Срок действия куки в секундах (по умолчанию: 0)
  string $path = "",        // Путь на сервере, где куки будет доступна
  string $domain = "",      // Домен, на котором куки будет доступна
  bool $secure = false,     // Передача по защищенному соединению HTTPS (да/нет)
  bool $httponly = false    // Доступ через HTTP протокол (да/нет)
): bool


// Пример

#Установка куки( ключ  |    значение   |   путь   |  кука живет 10 секунд )
setcookie('cookie_key', 'cookie_value', path: '/', expires_or_options: time()+10);

#Удаление куки( ключ  |    значение   |   путь   |  убавляем время жизни - 10 сек )
setcookie('cookie_key', 'cookie_value', path: '/', expires_or_options: time()-10);

#ВАЖНО: Кука должна удаляться с теми же аргументами, как при установке